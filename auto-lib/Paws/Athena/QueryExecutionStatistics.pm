=head1 DESCRIPTION

The location of a manifest file that tracks file locations generated by
the query, the amount of data scanned by the query, and the amount of
time that it took the query to run.

=head1 ATTRIBUTES


=head2 DataManifestLocation => Str

  The location and file name of a data manifest file. The manifest file
is saved to the Athena query results location in Amazon S3. It tracks
files that the query wrote to Amazon S3. If the query fails, the
manifest file also tracks files that the query intended to write. The
manifest is useful for identifying orphaned files resulting from a
failed query. For more information, see Working with Query Output Files
(https://docs.aws.amazon.com/athena/latest/ug/querying.html) in the
I<Amazon Athena User Guide>.


=head2 DataScannedInBytes => Int

